package com.example.java;

public class Colour {
    public String RED;
    public String GREEN;
    public String VIOLET;
    public String INDIGO;
    public String BLUE;
    public String YELLOW;
    public String ORANGE;
    public String BROWN;
    public String WHITE;
    public String BLACK;

    public Colour(String RED, String GREEN, String VIOLET, String INDIGO, String BLUE, String YELLOW, String ORANGE, String BROWN, String WHITE, String BLACK) {
        this.RED = RED;
        this.GREEN = GREEN;
        this.VIOLET = VIOLET;
        this.INDIGO = INDIGO;
        this.BLUE = BLUE;
        this.YELLOW = YELLOW;
        this.ORANGE = ORANGE;
        this.BROWN = BROWN;
        this.WHITE = WHITE;
        this.BLACK = BLACK;
    }

    public String getRED() {
        return RED;
    }

    public String getGREEN() {
        return GREEN;
    }

    public String getVIOLET() {
        return VIOLET;
    }

    public String getINDIGO() {
        return INDIGO;
    }

    public String getBLUE() {
        return BLUE;
    }

    public String getYELLOW() {
        return YELLOW;
    }

    public String getORANGE() {
        return ORANGE;
    }

    public String getBROWN() {
        return BROWN;
    }

    public String getWHITE() {
        return WHITE;
    }

    public String getBLACK() {
        return BLACK;
    }
}
